#include "minishell.h"

static void	prt(char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	write(1, s, i);
}

int	main(void)
{
	char	*s;

	s = readline("prompt> ");
	prt(s);
	free(s);
}
